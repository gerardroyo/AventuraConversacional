package com.AventuraConversacional;

public class Items {

    private String nom = "";
    private boolean usat = false;
    private String descripcio = "";

    public Items(String nom, boolean usat, String descripcio) {
    }

    public String getNom() {
        return nom;
    }

    public boolean getUsat() {
        return usat;
    }

    public String getDescripcio() {
        return descripcio;
    }
}
